const sectionByDate = (shiftsData, state) => {
  const sectioned = shiftsData.reduce((acc, curr) => {
    const date = new Date(curr.startTime).toDateString();
    const found = acc.find((item) => item.title === date);

    if (curr.length !== 0 && curr.booked === state) {
      if (!found) {
        acc.push({
          title: date,
          data: [curr],
        });
      } else {
        found.data.push(curr);
      }
    }

    return acc;
  }, []);

  return sectioned;
};

export const sectionedByDate = sectionByDate(shiftsData, true);

export const changeStatus = (id, status) => {
  shiftsData.map((item) => {
    if (item.id === id) {
      item.booked = status;
    }
  });
};

const sectionByPlace = (shiftsData) => {
  const sectioned = shiftsData.reduce((acc, curr) => {
    const found = acc.find((item) => item.title === curr.area);
    if (!found) {
      acc.push({
        title: curr.area,
        data: [curr],
      });
    } else {
      found.data.push(curr);
    }
    return acc;
  }, []);

  return sectioned;
};

export const sectionedByPlace = sectionByPlace(shiftsData).map((item) => {
  return {
    ...item,
    data: sectionByDate(item.data, false),
  };
});
